document.addEventListener('DOMContentLoaded', function() {
    var popup = document.getElementById("aiChatbotPopup");
    var btn = document.getElementById("openPopupBtn1");
    var span = document.getElementsByClassName("close-popup 1")[0];
  
    // Khi nút được nhấp, mở popup
    btn.onclick = function() {
      popup.style.display = "block";
    }
  
    // Khi dấu x được nhấp, đóng popup
    span.onclick = function() {
      popup.style.display = "none";
    }
  
    // Khi người dùng nhấp vào bất cứ đâu ngoài popup, đóng nó lại
    window.onclick = function(event) {
      if (event.target == popup) {
        popup.style.display = "none";
      }
    }
  });

